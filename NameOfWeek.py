
week = int(input("Enter week number (1-7): "))

if week == 1:
    print("Saturday")
elif week == 2:
    print("Sunday")
elif week == 3:
    print("Monday")
elif week == 4:
    print("Tuesday")
elif week == 5:
    print("Wednesday")
elif week == 6:
    print("Thursday")
elif week == 7:
    print("Friday")
else:
    print("Invalid!")
